// pop, shift

// pop
// it will delete last element of an array
let popArr = [1,2,3,4];
console.log(popArr, typeof popArr, popArr.length);

popArr.pop()
console.log(popArr, typeof popArr, popArr.length);

// shift
// it will delete first element
let popShift = [5,6,7,8,9];

popShift.shift()
console.log(popShift);