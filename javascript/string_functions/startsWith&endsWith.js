// startsWith()
// it will checks weather a string begins with specified characters.
// it returns true/ false (case sensitive)

let str = "Hey, Binod!";
console.log(str.startsWith("Hey")); //true
console.log(str.startsWith("hey")); //false

// endsWith
// it will check wether a string ends with specified characters.
// it returns true/ false 

console.log(str.endsWith('nod!'));  //true
console.log(str.endsWith('nod'));   //false